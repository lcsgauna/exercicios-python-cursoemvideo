valor = int(input("Digite um número: "))

valorDobro = valor * 2
valorTriplo = valor * 3
raizQuadrada = valor ** (1/2)

print("O dobro de {} vale {}".format(valor, valorDobro))
print("O triplo de {} vale {}".format(valor,valorTriplo))
print("A raiz quadrada de {} é igual a {:.2f}".format(valor,raizQuadrada))